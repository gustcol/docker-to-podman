# Generate all Quadlet files for a docker-compose project
generate_quadlet_files() {
    local file="$1"
    local project_name="$2"
    local output_dir="$3"
    local install="${4:-false}"

    log_info "Generating Quadlet files for project: $project_name"

    # Create output directory
    mkdir -p "$output_dir"

    # Generate default network
    generate_default_network_file "$project_name" "$output_dir"

    # Generate network files
    local networks
    networks=$(get_networks "$file")
    while IFS= read -r network; do
        [[ -z "$network" ]] && continue
        generate_network_file "$file" "$network" "$project_name" "$output_dir"
    done <<< "$networks"

    # Generate volume files
    local volumes
    volumes=$(get_volumes "$file")
    while IFS= read -r volume; do
        [[ -z "$volume" ]] && continue
        generate_volume_file "$file" "$volume" "$project_name" "$output_dir"
    done <<< "$volumes"

    # Generate container files
    local services
    services=$(get_services "$file")
    while IFS= read -r service; do
        [[ -z "$service" ]] && continue
        generate_container_file "$file" "$service" "$project_name" "$output_dir"
    done <<< "$services"

    log_success "All Quadlet files generated in: $output_dir"

    # Install if requested
    if [[ "$install" == "true" ]]; then
        install_quadlet_files "$output_dir"
    fi
}

# Install Quadlet files to systemd directory
install_quadlet_files() {
    local source_dir="$1"
    local mode="${2:-user}"

    local quadlet_dir
    quadlet_dir=$(ensure_quadlet_dir "$mode")

    log_info "Installing Quadlet files to: $quadlet_dir"

    # Copy all Quadlet files
    cp "$source_dir"/*.container "$quadlet_dir/" 2>/dev/null || true
    cp "$source_dir"/*.network "$quadlet_dir/" 2>/dev/null || true
    cp "$source_dir"/*.volume "$quadlet_dir/" 2>/dev/null || true

    # Reload systemd
    if [[ "$mode" == "user" ]]; then
        systemctl --user daemon-reload
        log_info "Reloaded user systemd daemon"
    else
        systemctl daemon-reload
        log_info "Reloaded system systemd daemon"
    fi

    log_success "Quadlet files installed successfully!"
    log_info "To start services, use: systemctl --user start <service-name>"
}

# List generated services
list_quadlet_services() {
    local project_name="$1"
    local mode="${2:-user}"

    log_info "Services for project '$project_name':"

    if [[ "$mode" == "user" ]]; then
        systemctl --user list-units --type=service "${project_name}-*" 2>/dev/null || true
    else
        systemctl list-units --type=service "${project_name}-*" 2>/dev/null || true
    fi
}

# Start all services for a project
start_quadlet_services() {
    local project_name="$1"
    local mode="${2:-user}"

    log_info "Starting all services for project: $project_name"

    local quadlet_dir
    quadlet_dir=$(get_quadlet_dir "$mode")

    # Start networks first
    for f in "${quadlet_dir}/${project_name}"*.network; do
        [[ -f "$f" ]] || continue
        local service_name
        service_name=$(basename "$f" .network)
        if [[ "$mode" == "user" ]]; then
            systemctl --user start "${service_name}-network.service" 2>/dev/null || true
        else
            systemctl start "${service_name}-network.service" 2>/dev/null || true
        fi
    done

    # Then start containers
    for f in "${quadlet_dir}/${project_name}"*.container; do
        [[ -f "$f" ]] || continue
        local service_name
        service_name=$(basename "$f" .container)
        log_info "Starting: $service_name"
        if [[ "$mode" == "user" ]]; then
            systemctl --user start "${service_name}.service"
        else
            systemctl start "${service_name}.service"
        fi
    done

    log_success "All services started!"
}

