# Export running containers as docker-compose.yml
export_to_compose() {
    local project_name="$1"
    local output_file="${2:-docker-compose.exported.yml}"

    log_info "Exporting containers for project: $project_name"

    # Get all containers for the project
    local containers
    containers=$(podman ps -a --filter "name=${project_name}" --format "{{.Names}}" 2>/dev/null)

    if [[ -z "$containers" ]]; then
        log_error "No containers found for project: $project_name"
        return 1
    fi

    cat > "$output_file" << 'EOF'
# Docker Compose file exported from Podman
# Generated by docker-to-podman migration tool
version: '3.8'

services:
EOF

    while IFS= read -r container; do
        [[ -z "$container" ]] && continue

        # Get container details using podman inspect
        local image
        image=$(podman inspect "$container" --format '{{.Config.Image}}' 2>/dev/null)

        # Service name (remove project prefix)
        local service_name="${container#"${project_name}"-}"
        service_name="${service_name#"${project_name}"_}"

        cat >> "$output_file" << EOF
  ${service_name}:
    image: ${image}
    container_name: ${container}
EOF

        # Add ports
        local port_bindings
        port_bindings=$(podman inspect "$container" --format '{{range $p, $conf := .NetworkSettings.Ports}}{{if $conf}}{{(index $conf 0).HostPort}}:{{$p}}{{"\n"}}{{end}}{{end}}' 2>/dev/null)
        if [[ -n "$port_bindings" ]]; then
            echo "    ports:" >> "$output_file"
            while IFS= read -r port; do
                [[ -z "$port" ]] && continue
                echo "      - \"${port}\"" >> "$output_file"
            done <<< "$port_bindings"
        fi

        # Add environment variables
        local env_list
        env_list=$(podman inspect "$container" --format '{{range .Config.Env}}{{.}}{{"\n"}}{{end}}' 2>/dev/null | grep -v '^PATH=' | grep -v '^HOME=' | grep -v '^HOSTNAME=' | head -20)
        if [[ -n "$env_list" ]]; then
            echo "    environment:" >> "$output_file"
            while IFS= read -r env; do
                [[ -z "$env" ]] && continue
                local key="${env%%=*}"
                local value="${env#*=}"
                value="${value//\\/\\\\}"
                value="${value//\"/\\\"}"
                echo "      ${key}: \"${value}\"" >> "$output_file"
            done <<< "$env_list"
        fi

        # Add volumes
        local mounts
        mounts=$(podman inspect "$container" --format '{{range .Mounts}}{{.Source}}:{{.Destination}}{{"\n"}}{{end}}' 2>/dev/null)
        if [[ -n "$mounts" ]]; then
            echo "    volumes:" >> "$output_file"
            while IFS= read -r mount; do
                [[ -z "$mount" ]] && continue
                echo "      - ${mount}" >> "$output_file"
            done <<< "$mounts"
        fi

        echo "" >> "$output_file"

    done <<< "$containers"

    log_success "Exported to: $output_file"
}
