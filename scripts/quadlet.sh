[[ -z "${_CONVERTER_LOADED:-}" ]] && source "$_LIB_DIR/converter.sh"

# Generate .container file for a service
generate_container_file() {
    local file="$1"
    local service="$2"
    local project_name="$3"
    local output_dir="$4"

    local container_name="${project_name}-${service}"
    local output_file="${output_dir}/${container_name}.container"

    # Get service properties using getter functions
    local image
    image=$(get_service_image "$file" "$service")
    if [[ -z "$image" ]]; then
        log_error "Service '$service' has no image defined"
        return 1
    fi
    image=$(normalize_image "$image")

    local ports volumes environment depends_on networks
    local command entrypoint restart memory cpus
    local env_file labels user working_dir
    local security_opt cap_add cap_drop privileged
    local network_mode ipc_mode pid_mode userns_mode
    local tmpfs_mounts devices read_only

    ports=$(get_service_ports "$file" "$service")
    volumes=$(get_service_volumes "$file" "$service")
    environment=$(get_service_environment "$file" "$service")
    depends_on=$(get_service_depends_on "$file" "$service")
    networks=$(get_service_networks "$file" "$service")
    command=$(get_service_command "$file" "$service")
    entrypoint=$(get_service_entrypoint "$file" "$service")
    restart=$(get_service_restart "$file" "$service")
    memory=$(get_service_resource "$file" "$service" "memory")
    cpus=$(get_service_resource "$file" "$service" "cpus")
    env_file=$(get_service_env_file "$file" "$service")
    labels=$(get_service_labels "$file" "$service")
    user=$(get_service_user "$file" "$service")
    working_dir=$(get_service_working_dir "$file" "$service")
    security_opt=$(get_service_security_opt "$file" "$service")
    cap_add=$(get_service_cap_add "$file" "$service")
    cap_drop=$(get_service_cap_drop "$file" "$service")
    privileged=$(get_service_privileged "$file" "$service")
    network_mode=$(get_service_network_mode "$file" "$service")
    ipc_mode=$(get_service_ipc "$file" "$service")
    pid_mode=$(get_service_pid "$file" "$service")
    userns_mode=$(get_service_userns_mode "$file" "$service")
    tmpfs_mounts=$(get_service_tmpfs "$file" "$service")
    devices=$(get_service_devices "$file" "$service")
    read_only=$(get_service_read_only "$file" "$service")

    cat > "$output_file" << EOF
# Quadlet container file for $service
# Generated by docker-to-podman migration tool
# Documentation: https://docs.podman.io/en/latest/markdown/podman-systemd.unit.5.html

[Unit]
Description=$service container (migrated from docker-compose)
EOF

    # Add dependencies
    if [[ -n "$depends_on" ]]; then
        while IFS= read -r dep; do
            [[ -z "$dep" ]] && continue
            echo "After=${project_name}-${dep}.service" >> "$output_file"
            echo "Requires=${project_name}-${dep}.service" >> "$output_file"
        done <<< "$depends_on"
    fi

    # Container section
    cat >> "$output_file" << EOF

[Container]
Image=$image
ContainerName=$container_name
EOF

    # Ports
    while IFS= read -r port; do
        [[ -z "$port" ]] && continue
        local parsed_port
        parsed_port=$(parse_port "$port")
        echo "PublishPort=$parsed_port" >> "$output_file"
    done <<< "$ports"

    # Volumes
    while IFS= read -r vol; do
        [[ -z "$vol" ]] && continue
        local converted_vol
        converted_vol=$(convert_volume_mount "$vol" "$project_name")
        echo "Volume=$converted_vol" >> "$output_file"
    done <<< "$volumes"

    # Environment variables
    while IFS= read -r env; do
        [[ -z "$env" ]] && continue
        echo "Environment=$env" >> "$output_file"
    done <<< "$environment"

    # Networks
    local has_network=false
    while IFS= read -r net; do
        [[ -z "$net" ]] && continue
        has_network=true
        echo "Network=${project_name}-${net}.network" >> "$output_file"
    done <<< "$networks"

    # If no network specified, use default pod network
    if [[ "$has_network" == "false" ]]; then
        echo "Network=${project_name}.network" >> "$output_file"
    fi

    # Resource limits
    [[ -n "$memory" ]] && echo "PodmanArgs=--memory=${memory}" >> "$output_file"
    [[ -n "$cpus" ]] && echo "PodmanArgs=--cpus=${cpus}" >> "$output_file"

    # Security options
    if [[ -n "$security_opt" ]]; then
        while IFS= read -r opt; do
            [[ -z "$opt" ]] && continue
            if [[ "$opt" == label:type:* ]]; then
                echo "SecurityLabelType=${opt#label:type:}" >> "$output_file"
            else
                echo "PodmanArgs=--security-opt=${opt}" >> "$output_file"
            fi
        done <<< "$security_opt"
    fi

    # Capabilities
    while IFS= read -r cap; do
        [[ -z "$cap" ]] && continue
        echo "AddCapability=${cap}" >> "$output_file"
    done <<< "$cap_add"

    while IFS= read -r cap; do
        [[ -z "$cap" ]] && continue
        echo "DropCapability=${cap}" >> "$output_file"
    done <<< "$cap_drop"

    # Privileged mode
    if [[ "$privileged" == "true" ]]; then
        echo "SecurityLabelType=spc_t" >> "$output_file"
        echo "PodmanArgs=--privileged" >> "$output_file"
    fi

    # Namespace modes
    if [[ -n "$network_mode" ]]; then
        case "$network_mode" in
            host) echo "Network=host" >> "$output_file" ;;
            none) echo "Network=none" >> "$output_file" ;;
            service:*) echo "PodmanArgs=--network=container:${network_mode#service:}" >> "$output_file" ;;
            container:*) echo "PodmanArgs=--network=${network_mode}" >> "$output_file" ;;
        esac
    fi

    [[ -n "$ipc_mode" ]] && echo "PodmanArgs=--ipc=${ipc_mode}" >> "$output_file"
    [[ -n "$pid_mode" ]] && echo "PodmanArgs=--pid=${pid_mode}" >> "$output_file"
    [[ -n "$userns_mode" ]] && echo "PodmanArgs=--userns=${userns_mode}" >> "$output_file"

    # Tmpfs mounts
    while IFS= read -r tmpfs; do
        [[ -z "$tmpfs" ]] && continue
        echo "Tmpfs=${tmpfs}" >> "$output_file"
    done <<< "$tmpfs_mounts"

    # Devices
    while IFS= read -r device; do
        [[ -z "$device" ]] && continue
        echo "AddDevice=${device}" >> "$output_file"
    done <<< "$devices"

    # Read-only root filesystem
    [[ "$read_only" == "true" ]] && echo "ReadOnly=true" >> "$output_file"

    # User
    [[ -n "$user" ]] && echo "User=${user}" >> "$output_file"

    # Working directory
    [[ -n "$working_dir" ]] && echo "WorkingDir=${working_dir}" >> "$output_file"

    # Labels
    while IFS= read -r label; do
        [[ -z "$label" ]] && continue
        echo "Label=$label" >> "$output_file"
    done <<< "$labels"

    # Environment files
    while IFS= read -r ef; do
        [[ -z "$ef" ]] && continue
        # Convert relative paths to absolute
        if [[ "$ef" == ./* ]] || [[ "$ef" == ../* ]]; then
            local compose_dir
            compose_dir=$(dirname "$file")
            ef="$compose_dir/$ef"
        fi
        echo "EnvironmentFile=$ef" >> "$output_file"
    done <<< "$env_file"

    # Entrypoint
    [[ -n "$entrypoint" ]] && echo "Entrypoint=${entrypoint}" >> "$output_file"

    # Command
    [[ -n "$command" ]] && echo "Exec=${command}" >> "$output_file"

    # Health check
    local healthcheck_test
    healthcheck_test=$(get_service_healthcheck "$file" "$service" "test")
    if [[ -n "$healthcheck_test" ]]; then
        healthcheck_test=$(echo "$healthcheck_test" | sed 's/^\[//' | sed 's/\]$//' | sed 's/"//g' | sed 's/,/ /g')
        healthcheck_test=$(echo "$healthcheck_test" | sed 's/^CMD-SHELL //' | sed 's/^CMD //')
        echo "HealthCmd=$healthcheck_test" >> "$output_file"

        local interval timeout retries
        interval=$(get_service_healthcheck "$file" "$service" "interval")
        timeout=$(get_service_healthcheck "$file" "$service" "timeout")
        retries=$(get_service_healthcheck "$file" "$service" "retries")

        [[ -n "$interval" ]] && echo "HealthInterval=$interval" >> "$output_file"
        [[ -n "$timeout" ]] && echo "HealthTimeout=$timeout" >> "$output_file"
        [[ -n "$retries" ]] && echo "HealthRetries=$retries" >> "$output_file"
    fi

    # Performance optimizations
    echo "" >> "$output_file"
    echo "# Performance optimizations" >> "$output_file"
    echo "AutoUpdate=registry" >> "$output_file"

    # Service section
    cat >> "$output_file" << EOF

[Service]
Restart=$(convert_restart_policy "$restart")
TimeoutStartSec=300

[Install]
WantedBy=default.target
EOF

    log_success "Generated: $output_file"
}

